package oop_Inheriitance4_17;

public class Animal {
	

	public void canWalk() {
		System.out.println("Animal can walk");
	}
	

	public void canEat() {
		System.out.println("Animal can eat");
	}

	public void canTalk() {
		System.out.println("Animal can talk");
	}

	public void canRun() {
		System.out.println("Animal can run fast");
	}

	public void canSleep() {
		System.out.println("Animal can sleep");
	}

	public void canRegenerate() {
		System.out.println("Animal can regenerate body parts");
	}

	
}
